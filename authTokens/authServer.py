    def userTokenSpawn(this,uid,serverScope,expiry=time()+DEFAULT_TOKEN_EXPIRY_LENGTH):
        # Produce authHead+Tail pair: ie. one sha256 string split...
        authHead = None
        authTail = None

        while(True): # DoWhile
            hash = hashlib.sha256()
            hash.update(urandom(32)) # Sha256 should only produce 32 bytes (256bits)
            authFull = hash.hexdigest()

            authHead = authFull[:32]
            authTail = authFull[32:]

            # print(authFull)
            
            if not (this.authDb.cursor().execute(f""" SELECT authHead FROM tokens WHERE authHead = "{authHead}" """).fetchone()):
                break # If no authHead found, then generated...

        # print("prefail")
        # print(uid)
        this.authDb.cursor().execute(f""" INSERT INTO tokens (authHead,authTail,serverScope,id,expiry) VALUES ("{authHead}","{authTail}","{serverScope}",{uid},{expiry})""")
        # print("postfail")
        this.authDb.commit()

        return [authHead,authTail]
    ###

    def tokenAuth(this,authHead,sourceServer,timestamps,challenges,results,timeRecieveds=time()): # MUST be used in secure connection. Return false or authTail
        target = this.authDb.cursor().execute(f""" SELECT (authTail,serverScope,expiry,lastCalled) FROM tokens WHERE authHead = "{authHead}" """).fetchone()
        # If authHead exists
        if not target:
            print("No such token exists.")
            return False

        # scope matches
        if sourceServer != target[1]:
            print("Scope fail.")
            return False

        # Timestamp resolves
        if abs(timestamps - timeRecieveds) >= CHALLENGE_AUTH_TIME_SYNC_RANGE or timeRecieveds >= target[2]: # Ie. if timestamp out of range of recieve time OR recieved AFTER token expiry.
            print("Timestamp fail or token expired.")
            return False

        # Challange check
        # hash = hashlib.sha256()
        # hash.update((str(authHead)+str(target[0])+str(timestamps)+str(challenges)).encode('utf-8')) # head AND tail used for challenge...
        # testResult = hash.hexdigest()

        testResult = hashCompute(authHead,target[0],timestamps,challenges)

        if testResult == results:
            print("Token auth success.")
            return target[0]

        print("Token tail mismatch")
        return False
