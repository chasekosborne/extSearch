def hashCompute(*args): # Testing hash? Must be same for both client and server.
    netString = ""
    for each in args:
        netString += str(each)

    netString = netString.encode('utf-8')
    hash = hashlib.sha256()
    hash.update(netString)

    return hash.hexdigest()
###

def rngGenHash(): # Salt creation, challenge creation
    return hashCompute(urandom(32))
